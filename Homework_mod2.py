import random

def get_ascii_art():
    return (
        "           *\n"
        "          /|\\\n"
        "         /*|O\\\n"
        "        /*/|\\*\\\n"
        "       /X/O|*\\X\\\n"
        "      /*/X/|O\\*\\\n"
        "     /O/*/X|*\\O\\\n"
        "           |\n"
        "        \u2606 | \u2606\n"
        "          \"\n "
    )

def get_random_greeting():
    greetings = [
        "С Новым Годом! Пусть ваши соседи думают, что вы выиграли в лотерею, услышав ваш праздничный смех!",
        "С наступающим! Пусть ваше новогоднее желание исполнится быстрее, чем вы его загадаете!",
        "Поздравляем с Новым Годом! Надеюсь, вы подготовили место для всех потрясающих вещей, которые произойдут!",
        "С Новым Годом! Пусть шампанское будет только в бокалах, а не на полу!",
        "С наступающим! Пусть это будет годом побед и меньше грустных мемов!",
    ]
    return random.choice(greetings)

def main():
    print("Добро пожаловать в новогоднюю программу!")
    while True:
        name = input("Введите ваше имя, чтобы начать праздник (или напишите 'выход', чтобы завершить): ")
        if name.lower() == 'выход':
            print("Спасибо, что воспользовались нашей программой! Счастливого Нового Года!")
            break

        print("\n" + get_ascii_art())
        
        personalized_greeting = f"{get_random_greeting()} Счастливого года, {name}!"
        print(personalized_greeting)
        
        # Небольшая "стебная" деталь
        if random.choice([True, False]):
            print("\nP.S. Надеюсь, вы приготовили подарок Деду Морозу, ведь он давно работает без зарплаты!")
        else:
            print("\nP.S. Если ваши планы на год уже сорвались, не расстраивайтесь — впереди еще 364 дня!")
        print("\n" + "-" * 50 + "\n")

if __name__ == "__main__":
    main()
